    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        try {
            String sql = "SELECT * FROM login WHERE username='" + txt_username.getText() + "' && password ='"
                    + txt_password.getText() + "';";
            con = DriverManager.getConnection("jdbc:mysql://localhost/mbaknis", "root", "");
            pst = con.prepareStatement(sql);
            st = con.createStatement();
            rs = pst.executeQuery();
            if (rs.next()) {
                if (rs.getString("role").equals("Admin")) {
                    BerhasilLoginAdmin admin = new BerhasilLoginAdmin();
                    admin.setVisible(true);
                    this.setVisible(false);

                } else {
                    BerhasilLoginStaff staff = new BerhasilLoginStaff();
                    staff.setVisible(true);
                    this.setVisible(false);

                }

            } else {

                if (!animatorLogin.isRunning()) {
                    signIn = true;
                    String user = txt_username.getText().trim();
                    String pass = String.valueOf(txt_password.getPassword());
                    boolean action = true;
                    if (user.equals("")) {
                        txt_username.setHelperText("Harap masukkan username anda");
                        txt_username.grabFocus();
                        action = false;
                    }
                    if (pass.equals("")) {
                        txt_password.setHelperText("Harap masukkan password anda");
                        if (action) {
                            txt_password.grabFocus();
                        }
                        action = false;
                    }
                    if (action) {
                        GagalLogin login = new GagalLogin();
                        login.setVisible(true);
                        enableLogin(true);
                        txt_username.setText("");
                        txt_password.setText("");
                    }

                }
            }

            sql = "SELECT * FROM login where username = '" + txt_username.getText() + "' "
                    + "and password ='" + txt_password.getText() + "'";
            try {
                st = con.createStatement();
                rs = pst.executeQuery(sql);
                if (rs.next()) {
                    FormAdminDashboard.nameShow.setText(rs.getString(2));
                    FormAdminDashboard.roleShow.setText(rs.getString(4));
                    FormStaffDashboard.nameShow.setText(rs.getString(2));
                    FormStaffDashboard.roleShow.setText(rs.getString(4));

                } else {
                    System.out.println("Error");
                }
            } catch (Exception e) {
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
//            GagalRegister gr = new GagalRegister();
//            gr.setVisible(true);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

