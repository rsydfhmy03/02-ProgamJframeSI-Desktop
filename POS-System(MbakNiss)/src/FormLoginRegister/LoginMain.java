    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        try {
            String sql = "INSERT INTO login(username, password, role) VALUES (?,?,?)";
            con = DriverManager.getConnection("jdbc:mysql://localhost/mbaknis", "root", "");
            pst = con.prepareStatement(sql);
            pst.setString(1, txt_username1.getText());
            pst.setString(2, txt_password1.getText());
            pst.setString(3, String.valueOf(roleComboBox1.getSelectedItem()));
            pst.execute();

            if (!animatorLogin.isRunning()) {
                signIn = true;
                String user = txt_username1.getText().trim();
                String pass = String.valueOf(txt_password1.getPassword());
                boolean action = true;
                if (user.equals("")) {
                    txt_username1.setHelperText("Harap masukkan username anda");
                    txt_username1.grabFocus();
                    action = false;
                }
                if (pass.equals("")) {
                    txt_password1.setHelperText("Harap masukkan password anda");
                    if (action) {
                        txt_password1.grabFocus();
                    }
                    action = false;
                }
                if (action) {
                    BerhasilRegister br = new BerhasilRegister();
                    br.setVisible(true);
                    signIn = false;
                    clearLogin();
                    animatorBody.start();
                }

            }
            txt_username1.setText("");
            txt_password1.setText("");

        } catch (Exception ex) {
            GagalRegister gr = new GagalRegister();
            gr.setVisible(true);
//            JOptionPane.showMessageDialog(this, ex);
        }

    }//GEN-LAST:event_button2ActionPerformed

